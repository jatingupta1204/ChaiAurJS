const month = 3

switch (month) {
    case 1:
        console.log("It's January");
        break;
    case 2:
        console.log("It's Feb");
        break;
    case 3:
        console.log("It's March");
        break;
    case 4:
        console.log("It's April");
        break;
    case 5:
        console.log("It's May");
        break;
    default:
        console.log("Please Enter correct month");
        break;
}